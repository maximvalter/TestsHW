# Задание «Квадратное уравнение»

def discriminant(a, b, c):
    """
    Функция для нахождения дискриминанта уравнения ax^2 + bx + c = 0.
    """
    return b ** 2 - 4 * a * c


def solution(a, b, c):
    """
    Решает квадратное уравнение ax^2 + bx + c = 0.
    Возвращает список:
        [] — если нет решений,
        [x] — если одно решение,
        [x1, x2] — если два решения,
        ["Бесконечно много решений"] — если все коэффициенты равны 0.
    """
    if a == 0:
        if b == 0:
            return [] if c != 0 else ["Бесконечно много решений"]
        return [-c / b]

    d = discriminant(a, b, c)

    if d < 0:
        return []
    elif d == 0:
        return [-b / (2 * a)]
    else:
        x1 = (-b + d ** 0.5) / (2 * a)
        x2 = (-b - d ** 0.5) / (2 * a)
        return [x1, x2]